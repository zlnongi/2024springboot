package com.sample.spring.tv1;

public class LgTv {
    public LgTv() { // 메소드 하나 생성
        System.out.println("===> LgTv");
    }
    public void powerOn() { // void는 return값이 없다
        System.out.println("LgTv 전원 on");
    }
    public void powerOff() { // void는 return값이 없다
        System.out.println("LgTv 전원 off");
    }
    public void volumeOn() { // void는 return값이 없다
        System.out.println("소리 on");
    }
    public void volumeOff() { // void는 return값이 없다
        System.out.println("소리 off");
    }

}
